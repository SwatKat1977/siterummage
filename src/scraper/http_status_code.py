class HTTPStatusCode(object):

    # 200 − OK
    OK =  200

    # 400 − for Bad Request
    BadRequest = 400

    # 401 − for Unauthenticated
    Unauthenticated = 401

    # 403 − for Forbidden
    Forbidden = 403

    # 404 − for Not Found
    NotFound = 404

    # 406 − for Not Acceptable
    NotAcceptable = 406

    # 415 − for Unsupported Media Type
    UnsupportedMediaType = 415

    # 429 − Too Many Requests
    TooManyRequests = 429
